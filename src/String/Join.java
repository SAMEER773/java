package String;

public class Join {
	
	public static void main(String[] args) {

	    
	    String a = "Java ";
	    System.out.println("First String: " + a);

	 
	    String b = "Programming";
	    System.out.println("Second String: " + b);

	    String c = a.concat(b);
	    System.out.println("Joined String: " + c);
	  }

}
