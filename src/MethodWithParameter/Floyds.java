package MethodWithParameter;

public class Floyds {
	static void flo(int a,int k) {
		
		for(int i=1; i<=a; i++) {
			
			
			for(int j=1; j<=i; j++) {
				
				System.out.print(k +" ");
				k++;
					
			}
			
			System.out.println();
			
		
		
	}

		
	}
	
	public static void main(String[] args) {
		flo(5,1);
		
}

}
